    //Saves the inputted data and inserts it into the database
    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        // save and go to database screen
        Calendar inCal = Calendar.getInstance();
        inCal.set(Calendar.HOUR_OF_DAY, (int) spn_Hours.getValue());

        inCal.set(Calendar.MINUTE, (int) spn_Minutes.getValue());
        inCal.set(Calendar.SECOND, (int) spn_Seconds.getValue());

        if (validateResult() == false) {

        } else {

            db.insertResult(sID, rID, inCal);

            RDS.PopulateList();
            RDS.setVisible(true);
            this.setVisible(false);

        }


    }//GEN-LAST:event_btn_SaveActionPerformed
