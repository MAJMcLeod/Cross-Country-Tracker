    //Populates the screen using the Students database
    public void StudentScreenPopulate() {
        Student[] allStudents = db.getAllStudents();
        DefaultListModel l = new DefaultListModel();
        for (int x = 0; x < allStudents.length; x++) {
            l.addElement(allStudents[x]);

        }
        lst_Students.setModel(l);

    }
    
    
    //Validates the inputted data by checking if there is a student selected, returns a true/false value
    private boolean validateResult() {
        boolean isValid = true;
        if (sID == 0) {
            isValid = false;
            lbl_Warning1.setVisible(true);

        }
        return isValid;
    }

