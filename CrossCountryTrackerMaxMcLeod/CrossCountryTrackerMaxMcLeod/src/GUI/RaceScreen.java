//Opens the Race Details Screen
    private void lst_StudentsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lst_StudentsMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {

            String value = String.valueOf(lst_Students.getSelectedValue());

            String[] x = value.split(", ");
            int id = Integer.parseInt(x[0]);

            Race sel = db.getRaceFromID(id);

            RDS = new RaceDetailsScreen(this, sel);
            RDS.setVisible(true);
            this.setVisible(false);

        }
    }//GEN-LAST:event_lst_StudentsMouseClicked

