    //Populates the screen using the Races database
    public void RaceScreenPopulate() {
        Race[] allRaces = db.getAllRaces();
        DefaultListModel l = new DefaultListModel();
        for (int x = 0; x < allRaces.length; x++) {
            l.addElement(allRaces[x]);

        }
        lst_Students.setModel(l);

    }
